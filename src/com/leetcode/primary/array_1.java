package com.leetcode.primary;

public class array_1 {

    public static void main(String[] args) {
        int[] nums = {1, 1, 2};
        int num = removeDuplicates(nums);
    }

    private static int removeDuplicates(int[] nums){
        for (int i:
             nums) {
            System.out.println(i);
        }
        return 0;
    }
}

